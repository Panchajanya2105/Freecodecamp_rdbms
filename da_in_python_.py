# Load and prepare the data
df = pd.read_csv('fcc-forum-pageviews.csv', index_col='date', parse_dates=True)

# Clean the data (remove top 2.5% and bottom 2.5%)
df = df[
    (df['value'] >= df['value'].quantile(0.025)) &
    (df['value'] <= df['value'].quantile(0.975))
]


def draw_line_plot():
    # Copy the data
    df_line = df.copy()

    fig, ax = plt.subplots(figsize=(12, 6))
    ax.plot(df_line.index, df_line['value'], color='red')

    ax.set_title('Daily freeCodeCamp Forum Page Views 5/2016-12/2019')
    ax.set_xlabel('Date')
    ax.set_ylabel('Page Views')

    fig.savefig('line_plot.png')
    return fig


def draw_bar_plot():
    # Copy the data
    df_bar = df.copy()

    # Prepare data for bar plot: average page views by month and year
    df_bar['year'] = df_bar.index.year
    df_bar['month'] = df_bar.index.strftime('%B')

    df_bar = df_bar.groupby(['year', 'month'])['value'].mean().reset_index()

    # Create the bar plot
    fig, ax = plt.subplots(figsize=(12, 6))
    sns.barplot(data=df_bar, x='year', y='value', hue='month', ax=ax)

    ax.set_xlabel('Years')
    ax.set_ylabel('Average Page Views')
    ax.legend(title='Months', loc='upper left')

    fig.savefig('bar_plot.png')
    return fig


