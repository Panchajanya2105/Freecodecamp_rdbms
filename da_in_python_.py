import pandas as pd
import matplotlib.pyplot as plt
from scipy.stats import linregress

# 1. Import the data
df = pd.read_csv('epa-sea-level.csv')

def draw_plot():
    # 2. Create scatter plot
    fig, ax = plt.subplots(figsize=(10, 6))
    ax.scatter(df['Year'], df['CSIRO Adjusted Sea Level'], color='blue', label='Original Data')

    # 3. First line of best fit (using all data)
    slope, intercept, _, _, _ = linregress(df['Year'], df['CSIRO Adjusted Sea Level'])

    # Predict up to 2050
    years_extended = pd.Series(range(df['Year'].min(), 2051))
    ax.plot(years_extended,
            slope * years_extended + intercept,
            'r', label='Best Fit Line (All Data)')

    # 4. Second line of best fit (from year 2000 onwards)
    df_recent = df[df['Year'] >= 2000]
    slope_recent, intercept_recent, _, _, _ = linregress(df_recent['Year'], df_recent['CSIRO Adjusted Sea Level'])

    ax.plot(years_extended,
            slope_recent * years_extended + intercept_recent,
            'g', label='Best Fit Line (2000+)')

    # Labels and title
    ax.set_xlabel('Year')
    ax.set_ylabel('Sea Level (inches)')
    ax.set_title('Rise in Sea Level')

    ax.legend()

    # Save and return the figure
    fig.savefig('sea_level_plot.png')
    return fig