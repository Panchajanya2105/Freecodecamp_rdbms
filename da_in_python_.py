def calculate_demographic_data(print_data=True):
    # Fetch dataset from UCI using ucimlrepo
    adult = fetch_ucirepo(id=2)

    # Get features and targets
    X = adult.data.features
    y = adult.data.targets

    # Combine features and target into one DataFrame
    df = pd.concat([X, y], axis=1)

    # Rename the target column to 'salary' (freeCodeCamp expects this name)
    df = df.rename(columns={df.columns[-1]: 'salary'})

    # 1. How many people of each race are represented?
    race_count = df['race'].value_counts()

    # 2. Average age of men
    average_age_men = round(df[df['sex'] == 'Male']['age'].mean(), 1)

    # 3. Percentage of people with Bachelor's degree
    percentage_bachelors = round((df['education'] == 'Bachelors').mean() * 100, 1)

    # 4. Advanced education (Bachelors, Masters, Doctorate)
    higher_education = df['education'].isin(['Bachelors', 'Masters', 'Doctorate'])
    lower_education = ~higher_education

    # Percentage with salary >50K
    higher_education_rich = round((df[higher_education]['salary'] == '>50K').mean() * 100, 1)
    lower_education_rich = round((df[lower_education]['salary'] == '>50K').mean() * 100, 1)

    # 5. Minimum hours per week
    min_work_hours = df['hours-per-week'].min()

    # 6. Percentage of min workers who earn >50K
    num_min_workers = df[df['hours-per-week'] == min_work_hours]
    rich_percentage = round((num_min_workers['salary'] == '>50K').mean() * 100, 1)

    # 7. Country with highest percentage of >50K earners
    country_rich = df[df['salary'] == '>50K']['native-country'].value_counts()
    country_total = df['native-country'].value_counts()
    highest_earning_country_percentage = round((country_rich / country_total * 100).max(), 1)
    highest_earning_country = (country_rich / country_total * 100).idxmax()

    # 8. Most popular occupation for >50K earners in India
    top_IN_occupation = df[(df['native-country'] == 'India') &
                          (df['salary'] == '>50K')]['occupation'].value_counts().idxmax()

